/*
Create a function that takes a number as an argument, increments the number by +1 and returns the result.

//-- FOR TESTING --//
addition(0) ➞ 1

addition(9) ➞ 10

addition(-3) ➞ -2
*/

const addition = (a) => ++a;

console.log(addition(0)) // 1
console.log(addition(9)) // 10
console.log(addition(-3)) // -2